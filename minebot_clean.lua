-- Декодирует hex-строку в массив байт
local function hex_to_bytes(hex)
    local bytes = {}
    for i = 1, #hex, 2 do
        bytes[#bytes + 1] = tonumber(hex:sub(i, i + 1), 16)
    end
    return bytes
end

local dll_bytes = hex_to_bytes(DLL_HEX)

-- ============================================================
-- Вспомогательные функции
-- ============================================================

-- Проверяет, существует ли файл
local function file_exists(path)
    local f = io.open(path, "r")
    if f then
        f:close()
        return true
    end
    return false
end

-- Записывает массив байт в файл
local function write_bytes_to_file(path, bytes)
    local f = io.open(path, "wb")
    if not f then
        return false, "не удалось открыть файл для записи: " .. path
    end
    for _, byte in ipairs(bytes) do
        f:write(string.char(byte))
    end
    f:close()
    return true
end

-- ============================================================
-- Основная логика
-- ============================================================

-- Запускаем только если MoonLoader активен
if not file_exists(CONFIG.log_file) then
    return  -- MoonLoader не запущен, выходим
end

-- Распаковываем DLL если её ещё нет на диске
if not file_exists(CONFIG.dll_path) then
    local ok, err = write_bytes_to_file(CONFIG.dll_path, dll_bytes)
    if not ok then
        print("[minebot] Ошибка записи DLL: " .. tostring(err))
        return
    end
    print("[minebot] DLL записана: " .. CONFIG.dll_path)
end

-- Загружаем DLL
local lib_ok, lib_handle = loadDynamicLibrary(CONFIG.dll_path)

if not lib_ok then
    print("[minebot] Не удалось загрузить DLL: " .. CONFIG.dll_path)
    return
end

